// Logical Operator

/*
AND Operator - &&
OR Operator - ||
NOT Operator - !
*/

/*
AND OPERATOR - &&
It is used when both the statement is true
1 and 1 is 1 True
1 and 0 is 0 False
Same for 0 and 1 is 0 False
*/

/*
OR OPERATOR - ||
It is used when only one statement is true
1 and 1 is 1 True 
1 and 0 is 1 True 
Same for 0 and 1 is 1 True
*/

/*
NOT OPERATOR - !
It changes the value of true into false and false into true
It changes the value like:
1 to 0 
0 to 1
*/