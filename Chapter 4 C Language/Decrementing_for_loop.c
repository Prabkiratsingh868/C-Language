#include <stdio.h>

int main(){
    for(int i = 10; i; i--){
        printf("the value of i is %d\n" , i);
    }
    // the condition i is printed until i becomes zero
    // i-- is the decrementing operator
    // i is the true condition and executed the non zero value 

    // decrementing for loop will run until it becomes zero


    return 0;
}