#include <stdio.h>

int main(){
    int a = 100;
    int b = 20;
    printf("The addition of a and b is %d\n" , a+b); // addition + 

    printf("The substraction of a and b is %d\n" , a-b); // substraction -

    printf("The multiplication of a and b is %d\n" , a*b); //multiplication *

    printf("The division of a and b is %d\n" , a/b); // division /

    printf("The remainder when a divided by b is %d\n" , a%b); // modulas %
    // the remainder when 100%20 is 0 because it is divisible by 20

    return 0;

}